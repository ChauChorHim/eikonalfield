def train():
    parser = config_parser()
    parser.add_argument("--grid_size", type=int, default=64,
                        help='the size of ior field, number of the voxels per dim')
    parser.add_argument("--real_scale", type=float, default=0.2,
                        help='the length of the ior field in real life')
    args = parser.parse_args()

    # Load data
    K = None
    if args.dataset_type == 'llff':
        images, poses, bds, render_poses, i_test = load_llff_data(args.datadir, args.factor,
                                                                  recenter=True, bd_factor=.75,
                                                                  spherify=args.spherify)
        hwf = poses[0, :3, -1]
        poses = poses[:, :3, :4]
        print('Loaded llff', images.shape, render_poses.shape, hwf, args.datadir)
        if not isinstance(i_test, list):
            i_test = [i_test]

        if args.llffhold > 0:
            print('Auto LLFF holdout,', args.llffhold)
            i_test = np.arange(images.shape[0])[::args.llffhold]

        i_val = i_test
        i_train = np.array([i for i in np.arange(int(images.shape[0])) if
                            (i not in i_test and i not in i_val)])

        print('DEFINING BOUNDS')
        if args.no_ndc:
            near = np.ndarray.min(bds) * .9
            far = np.ndarray.max(bds) * 1.

        else:
            near = 0.
            far = 1.
        print('NEAR FAR', near, far)

    elif args.dataset_type == 'blender':
        images, poses, render_poses, hwf, i_split = load_blender_data(args.datadir, args.half_res, args.testskip)
        print('Loaded blender', images.shape, render_poses.shape, hwf, args.datadir)
        i_train, i_val, i_test = i_split

        near = 0.6
        far = 1.0

        if args.white_bkgd:
            images = images[..., :3] * images[..., -1:] + (1. - images[..., -1:])
        else:
            images = images[..., :3]

    else:
        print('Unknown dataset type', args.dataset_type, 'exiting')
        return

    # Cast intrinsics to right types
    H, W, focal = hwf
    H, W = int(H), int(W)
    hwf = [H, W, focal]

    if K is None:
        K = np.array([
            [focal, 0, 0.5 * W],
            [0, focal, 0.5 * H],
            [0, 0, 1]
        ])

    # Create log dir and copy the config file
    basedir = args.basedir
    expname = args.expname
    os.makedirs(os.path.join(basedir, expname), exist_ok=True)

    # Create nerf model
    render_kwargs_train, render_kwargs_test, start, grad_vars, optimizer = create_nerf(args)

    with torch.no_grad():
        testsavedir = os.path.join(basedir, expname,
                                   'renderonly_{}_{:06d}'.format('test' if args.render_test else 'path', start))
        os.makedirs(testsavedir, exist_ok=True)

        x_max = y_max = z_max = args.real_scale
        x_min = y_min = z_min = -args.real_scale
        X, Y = np.meshgrid(np.linspace(0, 1, args.grid_size),
                           np.linspace(0, 1, args.grid_size))
        X = X * (x_max - x_min) + x_min
        Y = Y * (y_max - y_min) + y_min
        Z = np.zeros_like(X)

        pts = np.stack([X, Y, Z], axis=-1).reshape((-1, 3))
        views = np.stack([np.zeros_like(X),
                          np.zeros_like(Y),
                          np.ones_like(Z)], axis=-1).reshape((-1, 3))

        N_rays = pts.shape[0]

        pts = torch.from_numpy(pts).to(torch.float32).to(device)
        views = torch.from_numpy(views).to(torch.float32).to(device)

        t_vals = torch.linspace(0., 1., steps=args.grid_size)
        z_vals = z_min * (1. - t_vals) + z_max * (t_vals)
        z_vals = z_vals.expand([N_rays, args.grid_size])

        pts = pts[..., None, :] + views[..., None, :] * z_vals[..., :, None]

        NeRF_fine = render_kwargs_train["network_fine"]

        raw = render_kwargs_train["network_query_fn"](pts, views, NeRF_fine)

        _, _, _, weights, _ = raw2outputs(raw, z_vals, views)

        pts = torch.reshape(pts, (-1, 3)).cpu().numpy()
        weights = torch.reshape(weights, (-1, 1)).cpu().numpy()

        mask = np.where(weights > 0)

        cloud = PyntCloud(pd.DataFrame(
            # same arguments that you are passing to visualize_pcl
            data=np.hstack((pts[mask[0]],
                            weights[mask][:, None],
                            weights[mask][:, None],
                            weights[mask][:, None])),
            columns=["x", "y", "z", "red", "green", "blue"]))

        cloud.to_file(os.path.join(args.datadir, f'nerf_voxel_grid_{args.grid_size}_{args.real_scale}.ply'))

        return

if __name__ == '__main__':
    torch.set_default_tensor_type('torch.cuda.FloatTensor')

    train()
